# Lembrete ao usuario
print('Lembre-se: numeros quebrados escrevem-se com ponto e não com virgula')

# Entrada de valores (pelo usuario) e conversão de string para float
v1 = input('Insira o primeiro numero: ')
v2 = input('Insira o segundo numero: ')
n1 = float(v1)
n2 = float(v2)

# Soma de valores
sum = n2 + n1

# Exibição de resultado (soma)
print(f'{sum} é resultante da soma dos dois valores informados')





