# Entrada de dados pelo usuario e conversão de dados de string para float
meter = input('Insira a metragem: ')
meterf = float(meter)

# Calculo de conversão de metros para centimetros baseado nos valores inseridos pelo usuario na variavel "meter"
centimeter = meterf*100

# Exibição do resultado do calculo da area do quadrado na variavel "centimeter" 
print(f'{centimeter} são os centimetros resultantes de {meter} metro(s)')





