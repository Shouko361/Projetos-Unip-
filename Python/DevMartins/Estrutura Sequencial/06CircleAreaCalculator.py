# Entrada de dados (pelo usuario) e conversão do valor de string para float
raio = input('Insira o raio do ciruclo: ')
raiof = float(raio)

# Declaração do valor de PI em forma de variavel integer
pi = 3.14159265358979323846

# Operação de calculo (Calculando a area de um circulo baseado nos dados de entrada inseridos pelo usuario)
result = pi*(raiof**2)

# Exibição do resultado da operação (area do circulo) na variavel "result"
print(f'A area do ciruclo é de {result}')


### DICIONARIO DE VARIAVEIS
# raio = entrada de dado do raio do circulo pelo usuario
# raiof = variavel raio de string para float
# pi = valor de pi