# Entrada de dados pelo usuario e conversão de string para float
side  = input('Insira, em centimetros, um lado do quadrado: ')
sidef = float(side)

# Operação para obter a area do quadrado, baseada nos dados inseridos pelo usuario na variavel "side" 
area = sidef**2

# Exibição do resultado resultante da operação realizada na variavel "area"
print(f'A area do quadrado em centimetro é de {area} e o dobro da area é {area*2}')





### DICIONARIO DE VARIAVEIS
# side = metragem de um lado do quadrado
# sidef = conversao da variavel side de string para float
# area = caclulo matematico para obter a area de um quadrado