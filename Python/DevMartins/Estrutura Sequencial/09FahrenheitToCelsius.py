# Entrada de dados pelo usuario e conversão de dados de string para float
F = input('Insira a temperatura em Fahrenheit: ')
Ff = float(F)

# Operação de calculo (Conversão de Fahrenheit para Celsius) de acordo com os dados da variavel "F"
Celsius = 5*((Ff-32)/9)

# Exibição de resultado da operação realizada na variavel "Celsius"
print(f'A temperatura {Ff} Fahrenheit é equivalente a {Celsius}ºC')





