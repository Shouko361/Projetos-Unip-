# Entrada de dados pelo usuario e conversão de dados de string para float
C = input('Insira a temperatura em Celsius: ')
Cf = float(C)

# Operação de calculo (Conversão de Celsius para Fahrenheit) de acordo com os dados da variavel "C"
Fahrenheit = 32+(Cf*1.8)

# Exibição de resultado da operação realizada na variavel "Fahrenheit"
print(f'A temperatura {Cf} Fahrenheit é equivalente a {Fahrenheit}ºC')





# DICIONARIO DE VARIAVEIS
# C = Temperatura em Celsius
# Cf = Variavel C de string para float
# Fahrenheit = Conversão de celsius para Fahrenheit usando a variavel Cf