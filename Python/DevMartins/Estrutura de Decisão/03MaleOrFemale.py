# Entrada de dados pelo usuario
sex = input('Selecione o sexo. Digite M para Masculino e F para Feminino: ')

# Estrutura condicional de acordo com dados da variavel "sex"
if sex == 'M':
    print('Masculino')
elif sex == 'F':
    print('Feminino')
else:
    print('Sexo invalido')




